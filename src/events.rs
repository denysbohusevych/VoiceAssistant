// src/events.rs

/// Команды, которые управляют жизненным циклом сессии
#[derive(Debug, Clone)]
pub enum PipelineAction {
    /// Пользователь зажал хоткей (старт записи и сбора контекста)
    StartSession { target_pid: u32 },
    /// Пользователь отпустил хоткей (конец записи)
    StopSession,
}

/// Результаты работы изолированных фоновых потоков (воркеров),
/// которые отправляются в главный Оркестратор.
#[derive(Debug)]
pub enum WorkerEvent {

    /// НОВОЕ: Команда Оркестратору создать пустую сессию для сбора данных
    SessionStarted(u32),

    // --- События Аудио и Транскрипции ---
    /// Промежуточный текст транскрипции (пока пользователь еще говорит)
    PartialTranscription(String),
    /// Финальный распознанный кусок текста
    FinalTranscription(String),
    /// Аудио-движок сообщил, что поток окончательно остановлен (можно собирать результат)
    AudioFinished,
    /// Ошибка в аудио-пайплайне
    AudioError(String),

    // --- События Контекста и Зрения ---
    /// Базовый контекст захвачен мгновенно (имя приложения, PID)
    ContextCaptured { app_name: String, ax_path_json: Option<String> },
    /// Долгий процесс OCR и анализ структуры экрана завершены (готовый Markdown)
    VisionProcessed(String),
    /// Ошибка при захвате контекста или работе зрения
    VisionError(String),
}